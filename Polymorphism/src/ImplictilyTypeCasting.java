 class ImplictilyTypeCasting {
	
//the process of converting lesser data values to the bigger data type we use Implicitly type casting.	

	public static void main(String[] args) {
		int i =1000;
		System.out.println(i);
		float f =i;
		System.out.println(f);
		double d = i;
		System.out.println(d);
		
		System.out.println("hello");

	}

}
