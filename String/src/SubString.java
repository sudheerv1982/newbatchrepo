
public class SubString {

	public static void main(String[] args) {
		String s1 = new String("quick brown fox jumps over the lazy dog");
		System.out.println("substring starting from inex 15:");
		System.out.println(s1.substring(15));
		System.out.println("substring starting from index 20 and 25:");
		System.out.println(s1.substring(20, 25));

	}

}
