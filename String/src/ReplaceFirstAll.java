
public class ReplaceFirstAll {

	public static void main(String[] args) {
		String s1 = "Hello to java programming";
		System.out.println(s1.replace("Hello", "Welcome"));
		System.out.println(s1.replaceFirst("java", "salesforce"));
		System.out.println(s1.replaceAll("to", "to the"));

	}

}
