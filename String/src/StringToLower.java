
public class StringToLower {

	public static void main(String[] args) {
		String t3 = "I LIKE GAMES";
		String t4 = t3.toLowerCase();
		System.out.println("Before lowercase:" +t3);
		System.out.println("After changing to lowercase:" +t4);
		String s1 = new String("I AM INTERESTED IN JAVA");
		System.out.println("The lowercase is:" +s1.toLowerCase());
		

	}

}
